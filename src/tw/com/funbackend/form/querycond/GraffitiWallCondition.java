package tw.com.funbackend.form.querycond;

public class GraffitiWallCondition {
	public String categoryQ;
	public String wallOwnerQ;
	public String posterQ;
	
	/**
	 * @return the categoryQ
	 */
	public String getCategoryQ() {
		return categoryQ;
	}
	/**
	 * @param categoryQ the categoryQ to set
	 */
	public void setCategoryQ(String categoryQ) {
		this.categoryQ = categoryQ;
	}
	/**
	 * @return the wallOwnerQ
	 */
	public String getWallOwnerQ() {
		return wallOwnerQ;
	}
	/**
	 * @param wallOwnerQ the wallOwnerQ to set
	 */
	public void setWallOwnerQ(String wallOwnerQ) {
		this.wallOwnerQ = wallOwnerQ;
	}
	/**
	 * @return the posterQ
	 */
	public String getPosterQ() {
		return posterQ;
	}
	/**
	 * @param posterQ the posterQ to set
	 */
	public void setPosterQ(String posterQ) {
		this.posterQ = posterQ;
	}	
}
